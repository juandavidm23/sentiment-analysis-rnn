"""Limpieza al dataset


"""

def limpieza_dataset(text):
    # 1. Quitar HTML y URLs
    text = re.sub(r'<.*?>', '', text)
    text = re.sub(r'http\S+', '', text)

    # 2. Normalización (Minúsculas y Contracciones)
    text = text.lower()
    text = re.sub(r"can't", "cannot", text)
    text = re.sub(r"n't", " not", text)
    text = re.sub(r"i'm", "i am", text)

    # 3. Limpieza de caracteres (Solo letras y espacios)
    text = re.sub(r'[^a-z\s]', '', text)

    # 4. Stopwords (Sin eliminar negaciones para proteger el sentimiento)
    stop_words = set(stopwords.words('english'))
    negaciones = {'not', 'no', 'never', 'neither', 'nor'}
    stop_words = stop_words - negaciones

    # 5. Lemmatización (Reducir a la raíz)
    lemmatizer = WordNetLemmatizer()
    tokens = [lemmatizer.lemmatize(w) for w in text.split() if w not in stop_words]

    return " ".join(tokens)

