from tensorflow.keras.models import Sequential
from tensorflow.keras.layers import (
    Embedding, SimpleRNN, LSTM, GRU,
    Bidirectional, Dense, Dropout
)

def crear_modelo_birnn(tipo_rnn='SimpleRNN',
                      vocab_size=MAX_WORDS,
                      embedding_dim=128,
                      max_len=MAX_LEN,
                      units_1=64,
                      units_2=32,
                      dropout_rate=0.3):
    """
    Crea un modelo Bidirectional RNN flexible.
    tipo_rnn puede ser 'SimpleRNN', 'LSTM' o 'GRU'.
    """
    capas_rnn = {
        'SimpleRNN': SimpleRNN,
        'LSTM': LSTM,
        'GRU': GRU
    }
    CapaRNN = capas_rnn[tipo_rnn]

    model = Sequential([
        Embedding(input_dim=vocab_size,
                  output_dim=embedding_dim,
                  input_length=max_len,
                  mask_zero=True),
        Bidirectional(CapaRNN(units_1, return_sequences=True)),
        Dropout(dropout_rate),
        Bidirectional(CapaRNN(units_2)),
        Dropout(dropout_rate),
        Dense(32, activation='relu'),
        Dropout(dropout_rate),
        Dense(1, activation='sigmoid')
    ], name=f'BiRNN_{tipo_rnn}')

    return model

# Creamos el modelo con SimpleRNN (la BiRNN pura)
